static 
uchar
reverse(uchar b)
{
	int i;
	uchar c;

	c = 0;
	for (i=0; i<8; i++) {
		c |= BIT(b, 7-i) << i;
	}
	return c;
}

/* http://www.fileformat.info/format/xbm/egff.htm and XCreatePixmapCursor(3)
 * http://man.cat-v.org/plan_9/2/graphics */
static
Cursor
convert(P9Cursor *c)
{
	uchar set[2*CurWidth], clr[2*CurWidth];
	int hotx, hoty, i;
	Pixmap src, mask;
	Cursor xc;

	hotx = -c->offset[0];
	hoty = -c->offset[1];
	for (i=0; i<2*CurWidth; i++) {
		set[i] = reverse(c->set[i]);
		clr[i] = reverse(c->clr[i] | c->set[i]);
	}
	src = XCreatePixmapFromBitmapData(dpy, root, (char *)set, CurWidth, CurWidth, 1, 0, 1);
	mask = XCreatePixmapFromBitmapData(dpy, root, (char *)clr, CurWidth, CurWidth, 1, 0, 1);
	xc = XCreatePixmapCursor(dpy, src, mask, &black, &white, hotx, hoty);
	XFreePixmap(dpy, src);
	XFreePixmap(dpy, mask);
	return xc;
}

void
initcursor(int x11)
{
	int i;
	XColor dummy;

	if (x11) {
		cursor[Normal] = XCreateFontCursor(dpy, XC_left_ptr);
		cursor[TopLeft] = XCreateFontCursor(dpy, XC_top_left_corner);
		cursor[Top] = XCreateFontCursor(dpy, XC_top_side);
		cursor[TopRight] = XCreateFontCursor(dpy, XC_top_right_corner);
		cursor[Right] = XCreateFontCursor(dpy, XC_right_side);
		cursor[BottomRight] = XCreateFontCursor(dpy, XC_bottom_right_corner);
		cursor[Bottom] = XCreateFontCursor(dpy, XC_bottom_side);
		cursor[BottomLeft] = XCreateFontCursor(dpy, XC_bottom_left_corner);
		cursor[Left] = XCreateFontCursor(dpy, XC_left_side);
		cursor[PickWin] = XCreateFontCursor(dpy, XC_draped_box);
		cursor[PickPoint] = XCreateFontCursor(dpy, XC_crosshair);
		cursor[MoveWin] = XCreateFontCursor(dpy, XC_fleur);
	} else {
		XAllocNamedColor(dpy, DefaultColormap(dpy, screen), "black", &black, &dummy);
		XAllocNamedColor(dpy, DefaultColormap(dpy, screen), "white", &white, &dummy);
		for (i=0; i<NCursors; i++) {
			//printcur(plan9cursor[i]);
			cursor[i] = convert(plan9cursor[i]);
		}
	}
}
